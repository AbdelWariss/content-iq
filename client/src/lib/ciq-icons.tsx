export function Ico({
  icon,
  size = 16,
  style,
  className,
}: {
  icon: React.ReactNode;
  size?: number;
  style?: React.CSSProperties;
  className?: string;
}) {
  return (
    <span
      className={`ico${className ? ` ${className}` : ""}`}
      style={{ width: size, height: size, ...style }}
    >
      {icon}
    </span>
  );
}

export function MicWave({
  size = "md",
  color,
  listening = true,
}: {
  size?: "sm" | "md" | "lg";
  color?: string;
  listening?: boolean;
}) {
  const heights =
    size === "lg"
      ? [30, 60, 90, 50, 100, 70, 35]
      : size === "sm"
        ? [30, 60, 80, 50, 90, 55, 40]
        : [30, 70, 100, 60, 85, 40, 55];
  const widthMap = { sm: 2, md: 3, lg: 4 };
  const heightMap = { sm: 14, md: 22, lg: 36 };
  return (
    <span
      className={`wave${listening ? "" : " static"}`}
      style={{ color: color ?? "currentColor", height: heightMap[size] }}
    >
      {heights.map((h, i) => (
        <i key={i} style={{ width: widthMap[size], height: `${h}%` }} />
      ))}
    </span>
  );
}
